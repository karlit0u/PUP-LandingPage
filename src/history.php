            <p>
                The Polytechnic University of the Philippines – Santa Rosa is  blessed to be in a city with more than 200 years of history, the Lion City of the South, officially known as Santa Rosa City in the province of Laguna. 
                Santa Rosa was named after its patron  Saint Rose of Lima of Peru, when it was initially established as municipality in January 18, 1792.  
                Santa Rosa City has witnessed and stood the test of times from the Spanish Period to the present day political era. It has also proven its resilience and generosity during the Covid 19 pandemic.   
                And during the growth of the city PUP Santa Rosa was born  with the goal  of providing quality public education to mold the mind , heart and hands of the young people of Santa Rosa.  
            </p>
            <p>
                On February  21, 2003 Municipal Mayor, Hon. Leon C. Arcillas signed a memorandum of agreement with Polytechnic University of the Philippines through University  President, Dr. Ofelia M. Carague.
                This historic occasion was the beginning of  quality public tertiary education  in Santa Rosa.
            </p>
            <p>
                Initially, the Campus  offered  courses in Information Technology (BSIT), Education (BSE), and  Entrepreneurial Management (BSEM) at the undergraduate level.   
                For graduate studies the Master in Educational Management (MEM)  Open University System was introduced to cater to the needs of  those teachers in elementary and secondary schools in the City of Santa Rosa  and nearby towns who would like to pursue higher education.   
            </p>
            <p>
                Resolution No. 54-2004 of the Santa Rosa Sangguniang Bayan dated April 21, 2004  gave provisions for the offering of additional courses in Associate in Health Science Education (AHSE) leading to Bachelor of Science in Nursing, Bachelor of Science in Accountancy (BSA)  Bachelor of Science in Electronics & Communication Engineering (BSECE).  
                Because of the changing demands for jobs and preferences of students, more feasible and relevant programs replaced the offerings of some programs.
            </p>
            <p>
                Resolution No. 27-2008 of the Santa Rosa Sangguniang Panglungsod dated February 11, 2008 approving the opening of new courses- Bachelor of Science in Industrial Engineering (BSIE), Bachelor of Science in Business Administration- Marketing Management and Master in Public Administration.
            </p>
            <p>
            Resolution No.87-2015 of the Santa Rosa Sangguniang Panglungsod dated April 30, 2015 approving the opening of new courses- Bachelor in Business Teacher Education Major in Business Technology and Livelihood Education (BTE) that will be a response to the needs of the community.
            </p>
            <p>
            Resolution No.0066-2018 of the Santa Rosa Sangguniang Panglungsod dated April 23, 2018 approving the opening of new courses- Bachelor of Secondary Education Major in Filipino.
            </p>
            <p>
                Resolution No.0120-2020 of the Santa Rosa Sangguniang Panglungsod dated November 25, 2020 approving the opening of new courses- Bachelor of Science in Management Accounting.
            </p>
